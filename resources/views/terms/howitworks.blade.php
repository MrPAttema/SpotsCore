        Hoe kom ik in aanmerking voor een vakantieverblijf? 
        De leden van onze vereniging kunnen elk jaar in het najaar een aanvraag doen via de website  om maximaal drie wensen voor het volgende seizoen kenbaar te maken.
        Als je alleen in de (school)vakantieperiode een huisje kunt huren, bijvoorbeeld in verband met schoolgaande kinderen, dan is het verstandig om 3 verschillende vakantieverblijven op te geven. De ervaring leert, dat je dan meestal om het andere jaar een huisje toegewezen krijgt. Als je slechts één huisje aanvraagt, dan is het zelfs mogelijk dat je na 2 of 3 keer uitgeloot te zijn, toch opnieuw geen huisje toegewezen krijgt.
    </p>

